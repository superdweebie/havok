            <h2>Custom font and size lists</h2>

            <p>You can change the font and size lists by overriding the appropriate store in di config. Eg:</p>
<pre class="prettyprint linenums">
di: {
    'havok/store/stores': {
        proxies: {
            font: {
                base: 'my/alternative/font/store',
                proxyMethods: [
                    'get',
                    'query'
                ]
            }
        }
    }
}
</pre>

