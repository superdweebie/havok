
        <section id="affix" title="Affix">
          <div class="page-header">
            <h1>Affix</h1>
          </div>

          <p>The content of an Affix widget will stay visible in the viewport when it's target element is also visible. By default, Affix will target the parent element.</p>

          <h2>Example</h2>
          <p>Fix a widget to always be visible. The nav to the left is an example.</p>

          <h3>Stand alone widget</h3>
          <p>Scroll the viewport to see the example working</p>
          <div class="bs-docs-example">
              <style>
                .affix-example1.affix {
                  top: 40px;
                }
                .affix-example1.affix-bottom {
                  position: absolute;
                  top: auto;
                  bottom: 14px;
                }
              </style>
              <div style="height: 300px">
                  <div
                      class="affix-example1"
                      data-dojo-type="havok/widget/Affix"
                      data-dojo-props="viewportOffset: {top: 40, bottom: 0}"
                  >
                      <button class="btn">I'm affixed!</button>
                  </div>
              </div>
          </div>
