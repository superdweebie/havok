               </tr>
               <tr>
                 <td>viewportOffset</td>
                 <td>object</td>
                 <td>{top: 0, bottom: 0}</td>
                 <td>Distance in pixels from the top and bottom of the viewport that affixing should be applied.</td>
               </tr>
              </tbody>
