          <h3>Affix Mixin</h3>
          <p>Affix behaviour can be mixed in to any widget. Simply use <code>data-dojo-mixins="havok/widget/_AffixMixin"</code></p>
          <div class="bs-docs-example">
              <div style="height: 300px">
                  <button
                      class="affix-example1"
                      data-dojo-type="havok/widget/ToggleButton"
                      data-dojo-mixins="havok/widget/_AffixMixin"
                      data-dojo-props="viewportOffset: {top: 40, bottom: 0}"
                  >
                      Affixed toggle
                  </button>
              </div>
          </div>
